import { describe, expect, it, vi } from 'vitest'

import { insertAutoGeneratedShifts } from '@/lib/coverage/auto-generated-shifts'

type InsertResult = {
  data: unknown
  error: { code?: string; message?: string } | null
}

function makeSupabase(insertResult: InsertResult) {
  const insertMock = vi.fn().mockReturnValue({
    select: vi.fn().mockResolvedValue(insertResult),
  })

  return {
    from: vi.fn().mockReturnValue({
      insert: insertMock,
    }),
    insertMock,
  }
}

describe('insertAutoGeneratedShifts', () => {
  const rows = [
    {
      cycle_id: 'cycle-1',
      user_id: 'therapist-1',
      date: '2026-03-22',
      shift_type: 'day' as const,
      status: 'scheduled' as const,
      role: 'staff' as const,
    },
  ]

  it('inserts generated shifts without using onConflict upsert', async () => {
    const supabase = makeSupabase({
      data: [{ id: 'shift-1' }],
      error: null,
    })

    const result = await insertAutoGeneratedShifts(supabase, rows)

    expect(result).toEqual({ insertedCount: 1, duplicateConflict: false, error: null })
    expect(supabase.insertMock).toHaveBeenCalledWith(rows)
  })

  it('treats duplicate key errors as concurrent conflicts', async () => {
    const supabase = makeSupabase({
      data: null,
      error: { code: '23505', message: 'duplicate key value violates unique constraint' },
    })

    const result = await insertAutoGeneratedShifts(supabase, rows)

    expect(result).toEqual({
      insertedCount: 0,
      duplicateConflict: true,
      error: { code: '23505', message: 'duplicate key value violates unique constraint' },
    })
  })
})
