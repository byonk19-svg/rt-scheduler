import type { ShiftRole, ShiftStatus } from '@/app/schedule/types'
import type { Json } from '@/lib/supabase/database.types'

type AutoGeneratedShiftRow = {
  cycle_id: string
  user_id: string | null
  date: string
  shift_type: 'day' | 'night'
  status: ShiftStatus
  role: ShiftRole
  assignment_status?: 'scheduled' | 'cancelled'
  unfilled_reason?: string | null
  status_note?: string | null
}

type InsertError = { code?: string; message?: string } | null

type AutoGeneratedShiftInsertClient = {
  from: (table: 'shifts') => {
    insert: (rows: AutoGeneratedShiftRow[]) => {
      select: (columns: string) => PromiseLike<{ data: Array<{ id: string }> | null; error: InsertError }>
    }
  }
}

type UnpublishedCycleShiftMutationClient = {
  rpc: (
    fn: 'app_insert_unpublished_cycle_shifts',
    args: {
      p_actor_id: string
      p_cycle_id: string
      p_shifts: Json
    }
  ) => PromiseLike<{ data: Array<{ id: string }> | null; error: InsertError }>
}

export async function insertAutoGeneratedShifts(
  supabase: AutoGeneratedShiftInsertClient,
  rows: AutoGeneratedShiftRow[]
): Promise<{ insertedCount: number; duplicateConflict: boolean; error: InsertError }> {
  if (rows.length === 0) {
    return { insertedCount: 0, duplicateConflict: false, error: null }
  }

  const { data, error } = await supabase.from('shifts').insert(rows).select('id')
  if (error) {
    return {
      insertedCount: 0,
      duplicateConflict: error.code === '23505',
      error,
    }
  }

  return {
    insertedCount: data?.length ?? 0,
    duplicateConflict: false,
    error: null,
  }
}

export async function insertUnpublishedCycleShifts(
  supabase: UnpublishedCycleShiftMutationClient,
  params: {
    actorId: string
    cycleId: string
    rows: AutoGeneratedShiftRow[]
  }
): Promise<{ insertedCount: number; duplicateConflict: boolean; error: InsertError }> {
  if (params.rows.length === 0) {
    return { insertedCount: 0, duplicateConflict: false, error: null }
  }

  const { data, error } = await supabase.rpc('app_insert_unpublished_cycle_shifts', {
    p_actor_id: params.actorId,
    p_cycle_id: params.cycleId,
    p_shifts: params.rows as unknown as Json,
  })

  if (error) {
    return {
      insertedCount: 0,
      duplicateConflict: error.code === '23505',
      error,
    }
  }

  return {
    insertedCount: data?.length ?? 0,
    duplicateConflict: false,
    error: null,
  }
}
