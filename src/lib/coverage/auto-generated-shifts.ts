import type { ShiftRole } from '@/app/schedule/types'

type AutoGeneratedShiftRow = {
  cycle_id: string
  user_id: string
  date: string
  shift_type: 'day' | 'night'
  status: 'scheduled'
  role: ShiftRole
}

type InsertError = { code?: string; message?: string } | null

type AutoGeneratedShiftInsertClient = {
  from: (table: 'shifts') => {
    insert: (rows: AutoGeneratedShiftRow[]) => {
      select: (columns: string) => PromiseLike<{ data: Array<{ id: string }> | null; error: InsertError }>
    }
  }
}

export async function insertAutoGeneratedShifts(
  supabase: AutoGeneratedShiftInsertClient,
  rows: AutoGeneratedShiftRow[]
): Promise<{ insertedCount: number; duplicateConflict: boolean; error: InsertError }> {
  if (rows.length === 0) {
    return { insertedCount: 0, duplicateConflict: false, error: null }
  }

  const { data, error } = await supabase.from('shifts').insert(rows).select('id')
  if (error) {
    return {
      insertedCount: 0,
      duplicateConflict: error.code === '23505',
      error,
    }
  }

  return {
    insertedCount: data?.length ?? 0,
    duplicateConflict: false,
    error: null,
  }
}
